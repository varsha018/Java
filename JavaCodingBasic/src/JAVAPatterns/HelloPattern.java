package JAVAPatterns;

public class HelloPattern {

	public static void main(String[] args) {
		//       /"Hello"/
		
		System.out.println("/\"Hello\"/");  ///"Hello"/
		
		//   /'Hello'/
		
		System.out.println("/'Hello'/");
		
		System.out.println("\"/'Hello'/\""); //backslash before double quotes..
		
		//"I love "/JAVA/" and "/Programming/" with "/Selenium/""
		
		System.out.println("\"I love \"/JAVA/\" and \"/Programming/\" with \"/Selenium/\"\"");
	}

}
