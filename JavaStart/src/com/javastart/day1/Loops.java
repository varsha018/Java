package com.javastart.day1;

public class Loops {
	
	public static void main(String[] args) {
		
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i=i+1;
		}	
		System.out.println("************************");
		
		int j= 20;	
		for(j=20;j<=30;j++) {
			System.out.println(j);
			
		}
		System.out.println("************************");
		
		for(int k=10; k>=1 ;k--) {
			System.out.println(k);
		}
	}
	
	
	
	
 }
	
		

	

